# 0 1 2 3 4 5 6 7 8
# a b c d e f g h i

s = "abcdefghi"

print("Original \t= "+s+"\n")

print("s[3:6] \t\t= " + s[3:6])

print("s[3:6:2] \t= "+s[3:6:2]) # prints forward with 2 steps

print("s[::] \t\t= "+s[::])  # prints complete string

print("s[::-1] \t= "+s[::-1])  # prints in reverse

print("s[4:1:-2] \t= "+s[4:1:-2]) # prints in reverse with 2 steps


